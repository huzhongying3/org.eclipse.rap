/** <p>Tests the rendering for org.eclipse.rap.dhtml.TreeView (and thus for
 * TreeNode and TreeLeaf as well) for Navigator 6up.</p>
 */
public class TreeViewRenderer_Noscript_Test extends RenderingTestCase {
  
  public TreeViewRenderer_Noscript_Test( final String name ) {
    super( name );
    setGenerateResources( false );
  }
  
  protected void setUp() throws Exception {
    super.setUp();
    W4TFixture.setPrivateField( ImageCache.class, null, "_instance", null );
    ImageCache.createInstance( W4TFixture.getWebAppBase().toString(), 
                               IInitialization.NOSCRIPT_SUBMITTERS_NONE );
  }
  public void tearDown() throws Exception {
    super.tearDown();
    W4TFixture.setPrivateField( ImageCache.class, null, "_instance", null );
  }
   
   // testing methods
   //////////////////

   public void testRender() throws Exception {
     // reset the component counter, so that we have always the same IDs
     resetWebComponentCounter();
     // needed 
     WebForm testForm = new WebForm() {    
       protected void setWebComponents() throws Exception {      
       }
     };
     
     TreeView tv = new TreeView();
     testForm.add( tv, WebBorderLayout.CENTER );
     
     // node without children
     TreeNode tn1 = new TreeNode();
     tn1.setLabel( "TreeNode 1" );
     tv.addItem( tn1 );
     
     // node with child nodes
     TreeNode tn2 = new TreeNode();
     tn2.setLabel( "TreeNode 2" );
     tv.addItem( tn2 );

     TreeNode tn2_1 = new TreeNode();
     tn2_1.setLabel( "TreeNode 2_1" );
     tn2.addItem( tn2_1 );
     tn2.addWebActionListener( new WebActionListener() {
       public void webActionPerformed( final WebActionEvent e ) {
       }
     });

     TreeNode tn2_1_1 = new TreeNode();
     tn2_1_1.setLabel( "TreeNode 2_1_1" );
     tn2_1.addItem( tn2_1_1 );

     TreeNode tn2_1_2 = new TreeNode();
     tn2_1_2.setLabel( "TreeNode 2_1_2" );
     tn2_1.addItem( tn2_1_2 );

     TreeLeaf tl2_1_3 = new TreeLeaf();
     tl2_1_3.setLabel( "TreeLeaf 2_1_3" );
     tn2_1.addItem( tl2_1_3 );

     TreeNode tn2_2 = new TreeNode();
     tn2_2.setLabel( "TreeNode 2_2" );
     tn2.addItem( tn2_2 );

     // node with child node and leaf
     TreeNode tn3 = new TreeNode();
     tn3.setLabel( "TreeNode 3" );
     tv.addItem( tn3 );

     TreeNode tn3_1 = new TreeNode();
     tn3_1.setLabel( "TreeNode 3_1" );
     tn3.addItem( tn3_1 );

     TreeLeaf tl3_2 = new TreeLeaf();
     tl3_2.setLabel( "TreeLeaf 3_2" );
     tn3.addItem( tl3_2 );
     
     // needed for renderer loading    
     W4TFixture.fakeBrowser( new Default( false, false ) );


     // perform actual tests
     ///////////////////////
     doRenderTest( tv, 10 );
     
     // set a single node expanded 
     tn3.setExpanded( true );
     doRenderTest( tv, 11 );

     // change image set
     tv.setImageSetName( "resources/images/treeview/swing/Swing" );
     doRenderTest( tv, 12 );
     
     // change back the image set of a single node
     tn2_1.setImageSetName( "resources/images/treeview/modern/Modern" );
     doRenderTest( tv, 13 );
     // add webActionEventListener for tree leaf
     tv.addWebActionListener( new WebActionListener() {
       public void webActionPerformed( WebActionEvent e ) {
         // do nothing
       }
     });
     doRenderTest( tv, 14 );
     // add dragDropListener
     tv.addDragDropListener( new DragDropListener() {
       public void receivedDragDrop( DragDropEvent evt ) {
         // do nothing
       }
     } );
     doRenderTest( tv, 15 );
   }
}
