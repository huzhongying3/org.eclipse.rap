public class TreeViewRenderer_Default_Script_Test extends RenderingTestCase {

  public TreeViewRenderer_Default_Script_Test( final String name ) {
    super( name );
    setGenerateResources( false );
  }
  
  protected void setUp() throws Exception {
    super.setUp();
    W4TFixture.setPrivateField( ImageCache.class, null, "_instance", null );
  }
  
  public void tearDown() throws Exception {
    super.tearDown();
    W4TFixture.setPrivateField( ImageCache.class, null, "_instance", null );
  }
  
  // testing methods
  //////////////////

  public void testMarkupAppender() throws Exception {
    WebForm form = W4TFixture.getEmptyWebFormInstance();
    TreeView treeView = new TreeView();
    form.add( treeView, WebBorderLayout.CENTER );
    HtmlResponseWriter outDummy = new HtmlResponseWriter();
    W4TFixture.fakeBrowser( new Ie5up( true ) );
    ContextProvider.getStateInfo().setResponseWriter( outDummy );
    W4TFixture.renderComponent( treeView );
    HtmlResponseWriter outExpected = new HtmlResponseWriter();
    ContextProvider.getStateInfo().setResponseWriter( outExpected );
    WebRenderEvent evt;
    evt = new WebRenderEvent( form, WebRenderEvent.AFTER_RENDER );
    evt.processEvent();
    String markup = W4TFixture.getAllMarkup( outExpected );
    String expected 
      = "<script type=\"text/javascript\">" 
      + "try {document.body.onmouseup = dragDropHandler.clearDragDrop;" 
      + "var jtree=document.getElementById(\"p2\");jtree.style.height=\"0px\";" 
      + "} catch( e ) { }" 
      + "</script>";
    assertEquals( expected, markup );
  }
  
  public void testEncoding() throws Exception {
    // needed 
    WebForm testForm = new WebForm() {    
      protected void setWebComponents() throws Exception {      
      }
    };
    TreeView tv = new TreeView();
    testForm.add( tv, WebBorderLayout.CENTER );
    // node 
    TreeNode node = new TreeNode();
    node.setLabel( "TrööNädü" );
    node.setTitle( "Title of node:öäüß" );
    tv.addItem( node );
    TreeLeaf leaf = new TreeLeaf();
    leaf.setLabel( "ßöäpü" );
    tv.addItem( leaf );
    
    W4TFixture.fakeBrowser( new Default( true, false ) );
    W4TFixture.fakeResponseWriter();
    W4TFixture.renderComponent( tv );
    IServiceStateInfo stateInfo = ContextProvider.getStateInfo();
    String allMarkup = W4TFixture.getAllMarkup( stateInfo.getResponseWriter() );
    // test markup of node
    String expected 
      = "&nbsp;&nbsp;Tr&ouml;&ouml;N&auml;d&uuml;</span>";
    assertTrue( allMarkup.indexOf( expected ) != -1 );
    // test markup of leaf
    expected 
      = "&nbsp;&nbsp;Tr&ouml;&ouml;N&auml;d&uuml;</span>"; 
    assertTrue( allMarkup.indexOf( expected ) != -1 );
  }
  
  public void testRender() throws Exception {
    // reset the component counter, so that we have always the same IDs
    resetWebComponentCounter();
    // needed 
    WebForm testForm = new WebForm() {    
      protected void setWebComponents() throws Exception {      
      }
    };
    
    TreeView tv = new TreeView();
    testForm.add( tv, WebBorderLayout.CENTER );
    
    // node without children
    TreeNode tn1 = new TreeNode();
    tn1.setLabel( "TreeNode 1" );
    tv.addItem( tn1 );
    
    // node with child nodes
    TreeNode tn2 = new TreeNode();
    tn2.setLabel( "TreeNode 2" );
    tv.addItem( tn2 );

    TreeNode tn2_1 = new TreeNode();
    tn2_1.setLabel( "TreeNode 2_1" );
    tn2.addItem( tn2_1 );
    tn2.addWebActionListener( new WebActionListener() {
      public void webActionPerformed( final WebActionEvent e ) {
      }
    });

    TreeNode tn2_1_1 = new TreeNode();
    tn2_1_1.setLabel( "TreeNode 2_1_1" );
    tn2_1.addItem( tn2_1_1 );

    TreeNode tn2_1_2 = new TreeNode();
    tn2_1_2.setLabel( "TreeNode 2_1_2" );
    tn2_1.addItem( tn2_1_2 );

    TreeLeaf tl2_1_3 = new TreeLeaf();
    tl2_1_3.setLabel( "TreeLeaf 2_1_3" );
    tn2_1.addItem( tl2_1_3 );

    TreeNode tn2_2 = new TreeNode();
    tn2_2.setLabel( "TreeNode 2_2" );
    tn2.addItem( tn2_2 );

    // node with child node and leaf
    TreeNode tn3 = new TreeNode();
    tn3.setLabel( "TreeNode 3" );
    tv.addItem( tn3 );

    TreeNode tn3_1 = new TreeNode();
    tn3_1.setLabel( "TreeNode 3_1" );
    tn3.addItem( tn3_1 );

    TreeLeaf tl3_2 = new TreeLeaf();
    tl3_2.setLabel( "TreeLeaf 3_2" );
    tn3.addItem( tl3_2 );
    
    // needed for renderer loading    
    W4TFixture.fakeBrowser( new Default( true, false ) );


    // perform actual tests
    ///////////////////////
    doRenderTest( tv, 110 );
    
    // set a single node expanded 
    tn3.setExpanded( true );
    doRenderTest( tv, 111 );
    String[] expectedLibs = new String[] {
      "resources/js/treeview/doubleclick_script.js",
      "resources/js/treeview/treeview_default.js"
    };
    assertJSLibraries( expectedLibs );

    // change image set
    tv.setImageSetName( "resources/images/treeview/swing/Swing" );
    doRenderTest( tv, 112 );
    
    // change back the image set of a single node
    tn2_1.setImageSetName( "resources/images/treeview/modern/Modern" );
    doRenderTest( tv, 113 );
    
    // add webActionEventListener for tree leaf
    tv.addWebActionListener( new WebActionListener() {
      public void webActionPerformed( WebActionEvent e ) {
        // do nothing
      }
    });
    doRenderTest( tv, 114 );
    
    tv.addDragDropListener( new DragDropListener() {
      public void receivedDragDrop( DragDropEvent evt ) {
        // do nothing
      }
    } );
    doRenderTest( tv, 115 );
  }

  private void assertJSLibraries( final String[] expectedLibraries ) {
    IServiceStateInfo stateInfo = ContextProvider.getStateInfo();
    HtmlResponseWriter writer = stateInfo.getResponseWriter();
    String[] libraries = writer.getJSLibraries();
    assertEquals( expectedLibraries.length, libraries.length );
    for( int i = 0; i < libraries.length; i++ ) {
      assertEquals( expectedLibraries[ i ], libraries[ i ] );
    }
  }
  
}
