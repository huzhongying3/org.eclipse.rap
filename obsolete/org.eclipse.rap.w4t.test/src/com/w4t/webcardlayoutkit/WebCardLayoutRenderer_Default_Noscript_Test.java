import org.eclipse.rwt.internal.browser.Default;

import com.w4t.Renderer;
import com.w4t.WebCardLayout;
import com.w4t.util.RendererCache;

/** <p>Tests the rendering of org.eclipse.rap.WebCardLayout with a default no-script
 * browser.</p>
  */
public class WebCardLayoutRenderer_Default_Noscript_Test
  extends WebCardLayoutRendererBase
{

  public WebCardLayoutRenderer_Default_Noscript_Test( final String name ) {
    super( name );
    setGenerateResources( false );
  }
  
  public void testRenderingKit() {
    RendererCache rendererCache = RendererCache.getInstance();
    Renderer renderer = rendererCache.retrieveRenderer( WebCardLayout.class, 
                                                        browser );
    assertEquals( WebCardLayoutRenderer_Default_Noscript.class, 
                  renderer.getClass() );
  }
  
  public void testRender() throws Exception {
    runRenderTest();
  }
  
  protected void setUp() throws Exception {
    super.setUp();
    offset = 100;
    browser = new Default( false );
  }
}
